  getData(url, method, data = {}, header = {}) {
    return new Promise(function (resolve, reject) {
      wx.request({
        url: url,
        data: data,
        method: method,
        header: header,
        success: function (res) {
          if (res.statusCode == 200) {
            resolve(res.data);
          } else {
            reject('error');
          }
        },
        fail: function (e) {
          reject(e);
        }
      })
    })
  },
